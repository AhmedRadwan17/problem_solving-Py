num1=int(input("enter num1: "))
num2=int(input("enter num2: "))
Operator=input("enter Operator: (+,-,*,/,%) ")
if Operator=="+" :
    print(f"Num1+Num2:  {num1+num2}")
elif Operator=="-" :
    print(f"Num1-Num2: {num1-num2}")
elif Operator=="*" :
    print(f"Num1*Num2: {num1*num2}")
elif Operator=="/" :
        print(f"Num1/Num2: {num1/num2}")
elif Operator=="%" :
    print(f"Num1%Num2: {num1%num2}")